console.log("---math and rounding numbers---");
//round method
console.log("--- round method---");
console.log(Math.round(23.3));
console.log(Math.round(23.6));

// ceil method
console.log("---ceil method---");
console.log(Math.ceil(23.3));
console.log(Math.ceil(23.6));

console.log("---floor method---");
console.log(Math.floor(23.3));
console.log(Math.floor(23.6));

console.log("---Trunc method---");
console.log(Math.trunc(23.3));
console.log(Math.trunc(23.6));

console.log("---min method---");
console.log(Math.min(23, 12, 3, 4, 56, 78, 91, 0));
console.log(Math.min(23.6, 21, 32, 47, 2, 4, 6, 2, 1.0));

console.log("---sqrt method---");
console.log(Math.sqrt(25));
console.log(Math.sqrt(23));

console.log("----rounding decimals----");
console.log((2.8).toFixed(0));
console.log((2.9).toFixed(4));
